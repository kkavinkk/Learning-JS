const leapYear = (num) => num % 4 === 0;

console.log(leapYear('2014'));
console.log(leapYear('2015'));
console.log(leapYear('2016'));
console.log(leapYear('2017'));
console.log(leapYear('2018'));
console.log(leapYear('2019'));